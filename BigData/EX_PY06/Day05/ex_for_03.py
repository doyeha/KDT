# 출력 예식] 2 * 1 = 2

gugu = int(input("출력할 단을 입력해주세요. : "))
gugudan = str(gugu)+"단"

print("[{:-^10}]".format((gugudan)))
for i in range(1,10):
    print(gugu,"*", i, "= {:02}".format(gugu*i))


