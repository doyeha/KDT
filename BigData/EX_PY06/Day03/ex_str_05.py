# r' ' 또는 R' ' : 문자열 내 이스케이프 문자는 무시함 !
file = r"C:\Users\kjy19\.vscode\test.txt"
# 문자열 앞에 r | R 을 붙이면 해당 문자열 내 이스케이프문 은 다 무시

msg = "Happy\tNew\tyear"
msg2 = R"Happy\tNew\tyear"
print(msg, msg2)







