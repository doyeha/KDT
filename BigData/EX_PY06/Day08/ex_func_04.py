# 함수 기능 : 정수를 덧셈 한 후 결과를 반환/리턴하는 함수
# 함수 이름 : add
# 매개 변수 : 0~N개
# 함수 결과 : 덧셈 계산 값 result
# ------------------------------------------------------------------

def add(*nums): ## 가변인자 함수
    total = 0
    for n in nums:
        total += n
    return print(total)

