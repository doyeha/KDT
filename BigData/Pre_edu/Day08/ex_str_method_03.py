# 문자열의 대소문자 변환 메서드
# 메서드명 : upper() / lower()
# 형식 : 변수명.upper() / 변수명.lower()
# 결과 : 해당 형식으로 변환된 문자열 반환

data = "abc"
data1 = data.upper()
data2 = data1.lower()
print(data1, data2)
print



# 문자열의 첫번째 문자만 대문자, 나머지는 소문자로 변환 메서드
# 메서드명 : capitalize()
# 형식 : 변수명.capitalize()
# 결과 : 해당 형식으로 변환된 문자열 반환
data = "banAna"
data1 = data.capitalize()
print(data, data1)




